## List
teman = ['budi', 'epan', 'topek', 'maman'] ## <- Ini list
print(teman)

## .append()
teman = ['budi', 'epan', 'topek', 'maman']
teman.append('bambang') ## <- ini metode manambahkan
print(teman)

## del
teman = ['budi', 'epan', 'topek', 'maman']
del teman[1] ## <- ini metode menghapus
print(teman)

## .insert
teman = ['budi', 'epan', 'topek', 'maman']
teman.insert[2] ## <- ini metode insert
print(teman)
